    private void searchButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButton1ActionPerformed
        selectionSortCompany SortedList = new selectionSortCompany();
        List<inventoryMS> productSortedList = SortedList.sortByCompany(productList, false);
        binarySearch search = new binarySearch();
        inventoryMS data = search.searchByCompany(searchTxtField.getText().trim(),productSortedList, 0, productSortedList.size()-1);
        if (data!=null){
            JOptionPane.showMessageDialog(this, "Found","Found",JOptionPane.INFORMATION_MESSAGE);
            List<inventoryMS> tempoList=new ArrayList<>();
            tempoList.add(data);
            loadSortedProductListToTable(tempoList);
        }
        else{
            JOptionPane.showMessageDialog(this, "Data not Found","Not Found",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchButton1ActionPerformed
    
