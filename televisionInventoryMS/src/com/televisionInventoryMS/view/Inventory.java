    private void loadProductListToTable(){
        DefaultTableModel model = (DefaultTableModel) inventoryTbl.getModel();
        
        //Clear existing rows
        model.setRowCount(0);
        
        //Add rows from prodcutList
        for (inventoryMS product : productList){
            model.addRow(new Object[] {
                product.getModelNo(),
                product.getCompany(),
                product.getSize(),
                product.getPrice(),
                product.getResolution(),
                product.isSmart(),
                product.getInStock()
            });
        }
    }
    
    
