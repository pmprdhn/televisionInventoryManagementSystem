    private void sortButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortButtonActionPerformed
        int selectedIndex = sortComboBox.getSelectedIndex();
        if (selectedIndex == 0) {
            selectionSort sorter = new selectionSort();
            List<inventoryMS> productSortedList = sorter.sortByModelNo(productList, false);
            loadSortedProductListToTable(productSortedList);
        }
        else if (selectedIndex == 1) {
            insertionSort sorter = new insertionSort();
            List<inventoryMS> productSortedList = sorter.sortBySize(productList, false);
            loadSortedProductListToTable(productSortedList);
        }
        else if (selectedIndex==2){
            mergeSort sorter = new mergeSort();
            List<inventoryMS> productSortedList = sorter.sortByPrice(productList, false);
            loadSortedProductListToTable(productSortedList);
        }
        else if (selectedIndex==3){
            selectionSortCompany sorter = new selectionSortCompany();
            List<inventoryMS> productSortedList = sorter.sortByCompany(productList, false);
            loadSortedProductListToTable(productSortedList);
        }
        
        
    }//GEN-LAST:event_sortButtonActionPerformed

